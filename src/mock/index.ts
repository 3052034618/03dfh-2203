const generateTemperatureData = (baseTemp: number, variance: number, hours: number): { timestamps: string[]; temperatures: number[] } => {
  const timestamps: string[] = [];
  const temperatures: number[] = [];
  const startDate = new Date("2025-06-20T08:00:00");
  
  for (let i = 0; i < hours * 2; i++) {
    const time = new Date(startDate.getTime() + i * 30 * 60 * 1000);
    timestamps.push(time.toISOString());
    
    let temp = baseTemp + (Math.random() - 0.5) * variance;
    
    if (i === 8 || i === 16) {
      temp += 3 + Math.random() * 2;
    }
    
    temperatures.push(Math.round(temp * 10) / 10);
  }
  
  return { timestamps, temperatures };
};

